DROP DATABASE IF EXISTS BrumaCursos;
CREATE DATABASE IF NOT EXISTS BrumaCursos;
USE BrumaCursos;

CREATE TABLE Curso (
	idCurso 		INT 				NOT NULL PRIMARY KEY AUTO_INCREMENT,
	nome 			VARCHAR(40)			NOT NULL,
	descricao 		TEXT,
	carga_horaria 	INT(10) UNSIGNED DEFAULT NULL,
	ano_lancamento 	YEAR DEFAULT '2022'
) DEFAULT CHARSET = utf8;


CREATE TABLE Nacionalidade(
	idNacionalidade 	INT 		NOT NULL PRIMARY KEY AUTO_INCREMENT,
    pais				VARCHAR(30) NOT NULL
);

CREATE TABLE Profissao(
	idProfissao 	INT NOT NULL PRIMARY KEY AUTO_INCREMENT,
    nome 			VARCHAR(30) NOT NULL    
) DEFAULT CHARSET = utf8;

CREATE TABLE Pessoa (
	idPessoa 			INT 			NOT NULL PRIMARY KEY AUTO_INCREMENT,
	nome 				VARCHAR(30) 	NOT NULL,
	idProfissao 		INT 			NOT NULL,
	nascimento 			DATE 			DEFAULT NULL,
	sexo 				ENUM('M','F') 	DEFAULT NULL,
	peso 				DECIMAL(5,2) 	DEFAULT NULL,
	altura 				DECIMAL(3,2) 	DEFAULT NULL,
	idNacionalidade 	INT 			NOT NULL DEFAULT 100,
	FOREIGN KEY (idNacionalidade) REFERENCES Nacionalidade(idNacionalidade),
	FOREIGN KEY (idProfissao) 	REFERENCES Profissao(idProfissao)
) DEFAULT CHARSET = utf8;

CREATE TABLE Estudante (
	idEstudante 		INT 		NOT NULL PRIMARY KEY AUTO_INCREMENT,
	idPessoa 			INT 		NOT NULL,
	idCurso				INT 		NOT NULL,
	numero_matricula	VARCHAR(12) NOT NULL UNIQUE,
    data_matricula		DATE		NOT NULL,
	FOREIGN KEY (idPessoa)	REFERENCES Pessoa(idPessoa),
	FOREIGN KEY (idCurso)	REFERENCES Curso(idCurso)
);

CREATE TABLE Aula (
	idAula					INT 		NOT NULL PRIMARY KEY AUTO_INCREMENT,
    idCurso					INT			NOT NULL,
    data_aula				DATE		NOT NULL,
    conteudo				TINYTEXT	NOT NULL,
    quantidade_de_aulas		TINYINT		NOT NULL DEFAULT 0,
    FOREIGN KEY (idCurso)	REFERENCES Curso(idCurso)
);

CREATE TABLE Frequencia (
	idAula				 INT 		NOT NULL,
	idEstudante 		 INT 		NOT NULL,
    quantidade_de_faltas TINYINT NOT NULL DEFAULT 0,
    FOREIGN KEY (idAula) REFERENCES Aula(idAula),
    FOREIGN KEY (idEstudante) REFERENCES Estudante(idEstudante)
);


